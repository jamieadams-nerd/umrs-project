//! # CUI Color Palette
//!
//! Color palette definitions for rendering CUI marking strings in operator
//! terminal output.
//!
//! Palette entries map CUI marking classes to terminal color codes, enabling
//! consistent, visually distinguishable display of marking tiers.
//!
//! ## Key Exported Types
//!
//! This module is currently a placeholder for future palette definitions.
//! Color constants and palette mapping functions will be added here as the
//! UMRS label display layer matures.
//!
//! ## Compliance
//!
//! - **NIST SP 800-53 AC-16**: Security Attributes — consistent color coding
//!   of CUI markings reduces operator error when reading security-labelled
//!   output.
